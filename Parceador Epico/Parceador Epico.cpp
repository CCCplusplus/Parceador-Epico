int cube = 0;

struct CoordenadaYColor {
    int x, y, z;
    int color;
};

std::string decimalAHexadecimal(int numeroDecimal) {
    std::stringstream ss;
    ss << std::hex << numeroDecimal;
    return ss.str();
}

void convertirYMostrar(const std::vector<CoordenadaYColor>& datos) {
    for (const auto& item : datos) {
        std::cout << "0x" << decimalAHexadecimal(item.x);
        std::cout << decimalAHexadecimal(item.y) << ",\n";
        std::cout << "0x" << decimalAHexadecimal(item.z);
        std::cout << decimalAHexadecimal(item.color) << ", \n";
        cube++;
    }

    std::cout << cube;
}

void Organizar(const std::vector<std::string>& datos)
{
    for (size_t i = 0; i < datos.size(); ++i)
    {
        std::cout << datos[i];

        // Si no es el último elemento, imprimir una coma y un espacio
        if (i < datos.size() - 1)
        {
            std::cout << " ";
        }

        // Si es el 16º elemento o el último elemento del vector, insertar un salto de línea
        if ((i + 1) % 16 == 0 || i == datos.size() - 1)
        {
            std::cout << std::endl;
        }
    }
}

